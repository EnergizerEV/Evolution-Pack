function drawGrid() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    const size = 50;
    const accent = getComputedStyle(document.documentElement).getPropertyValue('--current-accent').trim() || '#00ffd8';
    ctx.strokeStyle = accent;
    ctx.lineWidth = 0.5;
    for (let x = 0; x < canvas.width; x += size) {
        for (let y = 0; y < canvas.height; y += size) {
            const dist = Math.hypot(x - mouse.x, y - mouse.y);
            const offset = Math.max(0, 10 - dist / 30);
            ctx.globalAlpha = offset > 0 ? 0.5 : 0.12;
            ctx.strokeRect(x - offset/2, y - offset/2, size + offset, size + offset);
        }
    }
    requestAnimationFrame(drawGrid);
}
