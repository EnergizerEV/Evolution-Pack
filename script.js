function fadeIn(audio, targetVol) {
    let vol = audio.volume;
    const interval = setInterval(() => {
        if (vol < targetVol) {
            vol += 0.01;
            audio.volume = vol.toFixed(2);
        } else {
            clearInterval(interval);
        }
    }, 50);
}

function fadeOut(audio) {
    let vol = audio.volume;
    const interval = setInterval(() => {
        if (vol > 0) {
            vol -= 0.01;
            audio.volume = vol.toFixed(2);
        } else {
            audio.pause();
            clearInterval(interval);
        }
    }, 50);
}
